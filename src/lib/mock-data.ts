import type { Product, PriceHistory, PriceListing, PlatformId } from './types'
import { subDays } from 'date-fns'

function genHistory(base: number, days = 30): PriceHistory[] {
  const history: PriceHistory[] = []
  for (let i = days; i >= 0; i--) {
    const variance = () => base * (0.85 + Math.random() * 0.25)
    history.push({
      date: subDays(new Date(), i),
      prices: {
        tokopedia:  i % 7 === 0 ? null : Math.round(variance() / 1000) * 1000,
        shopee:     Math.round(variance() * 0.94 / 1000) * 1000,
        lazada:     Math.round(variance() * 1.02 / 1000) * 1000,
        bukalapak:  i > 20 ? null : Math.round(variance() * 0.97 / 1000) * 1000,
        blibli:     Math.round(variance() * 1.05 / 1000) * 1000,
        tiktok:     i > 15 ? null : Math.round(variance() * 0.91 / 1000) * 1000,
        amazon:     null,
        alibaba:    null,
        aliexpress: null,
        jd:         null,
      },
    })
  }
  return history
}

function listing(
  platformId: PlatformId,
  price: number,
  opts: Partial<PriceListing> = {}
): PriceListing {
  const origMultiplier = 1 + (Math.random() * 0.3)
  const originalPrice = Math.round(price * origMultiplier / 1000) * 1000
  return {
    platformId,
    price,
    originalPrice,
    discount: Math.round(((originalPrice - price) / originalPrice) * 100),
    rating: 4.2 + Math.random() * 0.7,
    reviewCount: Math.floor(100 + Math.random() * 5000),
    sold: Math.floor(50 + Math.random() * 10000),
    stock: Math.floor(10 + Math.random() * 200),
    shopName: `Toko ${platformId.charAt(0).toUpperCase()}${platformId.slice(1)} Official`,
    shopVerified: Math.random() > 0.3,
    freeShipping: Math.random() > 0.4,
    url: `https://${platformId === 'tokopedia' ? 'tokopedia.com' : platformId === 'shopee' ? 'shopee.co.id' : platformId + '.co.id'}/product/mock-${platformId}`,
    affiliateUrl: `https://harga.app/go/${platformId}/mock-product`,
    imageUrl: `https://picsum.photos/seed/${platformId}/400/400`,
    updatedAt: new Date(),
    ...opts,
  }
}

