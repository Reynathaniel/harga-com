export function searchProducts(query: string, filters?: {
  category?: string
  minPrice?: number
  maxPrice?: number
}): Product[] {
  let results = MOCK_PRODUCTS

  if (filters?.category) {
    results = results.filter(p =>
      p.category.toLowerCase() === filters.category!.toLowerCase() ||
      p.subcategory.toLowerCase().includes(filters.category!.toLowerCase())
    )
  }

  if (query.trim()) {
    const q = query.toLowerCase()
    results = results.filter(p =>
      p.name.toLowerCase().includes(q) ||
      p.brand.toLowerCase().includes(q) ||
      p.tags.some(t => t.includes(q)) ||
      p.category.toLowerCase().includes(q) ||
      p.subcategory.toLowerCase().includes(q)
    )
  }

  if (filters?.minPrice) {
    results = results.filter(p => p.lowestPrice >= filters.minPrice!)
  }
  if (filters?.maxPrice) {
    results = results.filter(p => p.lowestPrice <= filters.maxPrice!)
  }

  return results
}

export function getProductById(id: string): Product | undefined {
  return MOCK_PRODUCTS.find(p => p.id === id || p.slug === id)
}

